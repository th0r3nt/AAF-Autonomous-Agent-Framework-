@llm_skill(
    # description: Подробно объясните агенту, зачем нужен этот инструмент и что он делает
    description="[Тестовый плагин]: Узнать текущую цену криптовалюты. Используй при вопросах о рынке.",
    
    # parameters: Описание аргументов функции для нейросети (формирует JSON Schema)
    parameters={
        "currency": {
            "description": "Тикер криптовалюты (на английском).", 
            "type": "string", # Тип данных (string, integer, boolean)
            "enum": ["btc", "eth", "sol", "ton"] # (Опционально) Жесткие рамки выбора
        },
        "convert_to": {
            "description": "В какую фиатную валюту конвертировать.",
            "type": "string",
            "enum": ["usd", "eur", "rub"]
        }
    }
)
# ВАЖНО: 
# 1. Всегда указывайте типы аргументов (currency: str) и тип возвращаемого значения (-> str)
# 2. Если у аргумента есть значение по умолчанию (="usd"), для ИИ он становится НЕОБЯЗАТЕЛЬНЫМ
async def get_crypto_price_example(currency: str, convert_to: str = "usd") -> str:
    
    # Логирование (появится в консоли с красивым префиксом)
    system_logger.info(f"[Plugin] Агент запросил цену {currency.upper()} в {convert_to.upper()}")
    
    # Если вашему скрипту нужны секретные ключи (например, от Binance), 
    # просто добавьте их в .env файл профиля вашего агента и вызывайте так:
    # api_key = os.getenv("BINANCE_API_KEY")
    
    # Имитация долгого запроса к внешнему API
    await asyncio.sleep(1) 
    
    # Заглушка базы данных
    mock_db = {
        "btc": {"usd": 95000, "eur": 88000, "rub": 9000000},
        "eth": {"usd": 3400, "eur": 3100, "rub": 320000},
        "sol": {"usd": 140, "eur": 130, "rub": 13500},
        "ton": {"usd": 5, "eur": 4, "rub": 480}
    }
    
    if currency.lower() not in mock_db:
        return f"Ошибка: Валюта {currency} не найдена в базе."
        
    price = mock_db[currency.lower()].get(convert_to.lower(), "N/A")
    
    # Инструмент ОБЯЗАН возвращать строку (str), которую прочитает LLM-модель
    return f"Текущая цена {currency.upper()}: {price} {convert_to.upper()}"


